    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // save add/less stock qty
        
        
        int newqty = Integer.valueOf(txt_newQty.getText());
        int oldqty = Integer.valueOf(txt_stkqty.getText());
        
        int up_qty = newqty + oldqty;
        
        
        try{
            Statement s = db.mycon().createStatement();
            
            s.executeUpdate("UPDATE product SET Qty='"+up_qty+"' WHERE pid='"+id+"' ");
            
            JOptionPane.showMessageDialog(null, "Product details updated !");
            
        }catch(HeadlessException | SQLException e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

