    private void txt_bcode1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_bcode1KeyReleased
        // Load unit price
        
        String name =txt_bcode1.getText();
        try{
            
            Statement s = db.mycon().createStatement();
            ResultSet rs = s.executeQuery("SELECT pid,Product_Name, Bar_code, Buy_price, SUM(Qty) FROM product WHERE Bar_code ='"+name+"'");
            if(rs.next()){
                
                
                id = rs.getString("pid");
                txt_pna1.setText(rs.getString("Product_Name"));
                txt_stkqty.setText(rs.getString("SUM(Qty)"));
                
                
            }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_txt_bcode1KeyReleased

